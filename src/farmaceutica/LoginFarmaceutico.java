    private void BtnLoginFarmaceuticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLoginFarmaceuticoActionPerformed
        // TODO add your handling code here:
        String emailMedico = TextEmailFarmaceutico.getText();
        String senhaMedico = TextSenhaFarmaceutico.getText();

        if (emailMedico.isEmpty() || senhaMedico.isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite o Email ou Senha!");
        }else{

            if(evt.getSource()==BtnLoginFarmaceutico)
            {
                Farmaceutico farmaceutico = new Farmaceutico();
                FarmaceuticoDAO farmaceuticoBD;

                farmaceutico.setEmail(TextEmailFarmaceutico.getText());
                farmaceutico.setSenha(TextSenhaFarmaceutico.getText());
                farmaceuticoBD = new FarmaceuticoDAO();

                if( farmaceuticoBD.LoginFarmaceutico(farmaceutico)){
                    TelaPrincipalFarmaceutico telaprincipal = new TelaPrincipalFarmaceutico();
                    telaprincipal.setVisible(true);
                    this.dispose();
                }

            }
        }
    }//GEN-LAST:event_BtnLoginFarmaceuticoActionPerformed

