    private void BtnRegistroPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistroPacienteActionPerformed
        // TODO add your handling code here:
        Paciente paciente = new Paciente();
        String idMedico = TextIdMedico.getText();
        String nomePaciente = TextNomePaciente.getText();
        String cpfPaciente = TextCpfPaciente.getText();

        if (nomePaciente.isEmpty() || cpfPaciente.isEmpty() || idMedico.isEmpty() ){
            JOptionPane.showMessageDialog(null, "Digite o Identificador do MÃ©dico, Nome do paciente e Cpf do paciente!");
        }else{
        
        if(evt.getSource()==BtnRegistroPaciente)
{
    
    PacienteDAO pacienteBD;

    paciente.setNome(TextNomePaciente.getText());
    paciente.setCpf(TextCpfPaciente.getText());
    paciente.setIdMedico(Integer.parseInt(TextIdMedico.getText()));
    pacienteBD = new PacienteDAO();
    pacienteBD.insert(paciente);
    
        
    
}}
    }//GEN-LAST:event_BtnRegistroPacienteActionPerformed

